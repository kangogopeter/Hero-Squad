public class Hero{

private String mName;
private int mSize;
private String mPersonal;
private String mGroup;

  public Hero(String name, Integer size, String personal, String group){

mName = name;
mSize = size;
mPersonal = personal;
mGroup = group;
}

public String getName(){
return mName;
}
public int getSize(){
return mSize;
}
public String getPersonal(){
return mPersonal;
}
public String getGroup(){
return mGroup;
           }
     }
